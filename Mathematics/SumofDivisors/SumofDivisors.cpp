#define int long long

void solve([[maybe_unused]] int test) {
    int n;
    scanf("%lld", &n);
    int ans = 0;
    for (int i = 1; i * i <= n; i++) {
        if (i * i != n) {
            ans += n / i;
            ans += i;
        } else {
            ans += i;
        }
    }
    printf("%lld\n", ans);
}

int32_t main() {
    int t = 1;
    // cin >> t;
    for (int tt = 1; tt <= t; tt++) {
        solve(tt);
    }
}
