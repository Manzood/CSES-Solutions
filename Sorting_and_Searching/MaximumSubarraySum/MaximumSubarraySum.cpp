using namespace std;
#define int long long

void solve([[maybe_unused]] int test) {
    int n;
    scanf("%lld", &n);
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        scanf("%lld", &a[i]);
    }
    int cur = a[0];
    int mx = cur;
    for (int i = 1; i < n; i++) {
        if (cur < 0) cur = 0;
        cur += a[i];
        mx = max(mx, cur);
    }
    printf("%lld\n", mx);
}

int32_t main() {
    int t = 1;
    // cin >> t;
    for (int tt = 1; tt <= t; tt++) {
        solve(tt);
    }
}
